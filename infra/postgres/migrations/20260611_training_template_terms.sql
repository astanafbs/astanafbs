UPDATE training_sessions
SET drills = (
    SELECT jsonb_agg(
      CASE drill->>'label'
        WHEN 'Разбой' THEN jsonb_set(drill, '{label}', to_jsonb('Разбой пирамиды'::text))
        WHEN 'Выход на шар' THEN jsonb_set(drill, '{label}', to_jsonb('Свояк, скат с точки'::text))
        WHEN 'Длинный удар' THEN jsonb_set(drill, '{label}', to_jsonb('Чужой прямой удар'::text))
        ELSE drill
      END
    )
    FROM jsonb_array_elements(training_sessions.drills) AS drill
  ),
  updated_at = now()
WHERE drills @> '[{"label":"Разбой"}]'::jsonb
   OR drills @> '[{"label":"Выход на шар"}]'::jsonb
   OR drills @> '[{"label":"Длинный удар"}]'::jsonb;
